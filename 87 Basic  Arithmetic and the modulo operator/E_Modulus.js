/*
The modulus operator returns the remainder left over when a dividend 
is divided by a divisor. The modulus operator is also known as 
the remainder operator. It takes the sign of the dividend.
*/

// Number % Number => Modulus of the number
let x = 9 % 5;
let y = -12 % 5;
let z = 1 % -2;
let a = 5.5 % 2;
let b = -4 % 2;
let c = NaN % 2;

console.log(x);
console.log(y);
console.log(z);
console.log(a);
console.log(b);
console.log(c);


//Output:-
/*
4
-2
1
1.5
-0
NaN
*/
