//This is a unary operator i.e. it operates on a single operand. 
// It gives the negation of an operand.

let a = 3;
b = -a; 

// Unary negation operator
// can convert non-numbers
// into a number
let x = "3";
y = -x; 
  
console.log(a);
console.log(b);
console.log(x);
console.log(y);

//Output:-

// 3
// -3
// 3
// -3